/**
 * Inline SVG icon components — eliminates lucide-react @types/react peer
 * dependency conflict in pnpm monorepos with mixed React 18/19 workspaces.
 */
import React from 'react';

export type IconProps = {
  size?: number;
  style?: React.CSSProperties;
  className?: string;
  strokeWidth?: number;
  color?: string;
};

function makeSvg(
  paths: React.ReactNode,
  viewBox = '0 0 24 24',
): (props: IconProps) => React.JSX.Element {
  function Icon({ size = 20, style, className, strokeWidth = 2, color }: IconProps) {
    return (
      <svg
        width={size}
        height={size}
        viewBox={viewBox}
        fill="none"
        stroke={color ?? 'currentColor'}
        strokeWidth={strokeWidth}
        strokeLinecap="round"
        strokeLinejoin="round"
        style={style}
        className={className}
      >
        {paths}
      </svg>
    );
  }
  return Icon;
}

